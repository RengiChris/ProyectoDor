export const prioridades = () => {
  let prioridades = [];
  posts.forEach((post) => {
    if (prioridades.indexOf(post.prioridad) === -1)
      prioridades.push(post.prioridad);
  });
  prioridades.push("Todos");
  return prioridades;
};

export const postsPerDate = (date) => {
  let postList = [];
  posts.forEach((post) => {
    if (post.date_start === date) postList.push(post);
  });
  return postList;
};

export const postsPerTendencia = (tag) => {
  const postList = posts.filter((post) =>
    post.tag.toLowerCase().includes(tag.toLowerCase())
  );
  return postList;
};

export const postsDates = () => {
  let dates = [];
  posts.forEach((post) => {
    dates.push(post.date_start);
  });
  return dates;
};

